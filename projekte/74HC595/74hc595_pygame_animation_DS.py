class Leitung:
    """Eine sichtbare Verbindungsleitung mit Zustand 0/1."""

    def __init__(self, name, points, value=0, width=4):
        self.name = name
        self.points = points
        self.value = 1 if value else 0
        self.width = width
        self.flash = 0

    def set(self, value):
        new_value = 1 if value else 0
        if new_value != self.value:
            self.flash = 14
        self.value = new_value

    def pulse(self):
        self.flash = 18

    def update(self):
        if self.flash > 0:
            self.flash -= 1

    def draw(self, screen):
        if self.flash > 0:
            color = YELLOW
            width = self.width + 4
        else:
            color = RED if self.value else GRAY
            width = self.width

        if len(self.points) == 2:
            pygame.draw.line(screen, color, self.points[0], self.points[1], width)
        else:
            pygame.draw.lines(screen, color, False, self.points, width)


class HC595:
    """Logikmodell des 74HC595 für die Animation."""

    def __init__(self):
        self.reset()

    def reset(self):
        self.shift = [0] * 8       # Schieberegister
        self.store = [0] * 8       # Speicher-/Ausgangsregister
        self.ds = 0
        self.oe = 0                # 0 = Ausgänge aktiv
        self.mr = 1                # 1 = normaler Betrieb
        self.qh_serial = 0
        self.step_count = 0
        self.last_action = "Reset: Schieberegister und Ausgangsregister gelöscht"

    def shift_clock(self, bit):
        """Steigende Flanke an SH_CP: ein Bit wird seriell eingeschoben."""
        self.ds = 1 if bit else 0
        self.qh_serial = self.shift[7]

        for i in range(7, 0, -1):
            self.shift[i] = self.shift[i - 1]
        self.shift[0] = self.ds

        self.step_count += 1
        self.last_action = f"↑ SH_CP: Bit {self.ds} eingeschoben, alle Bits rutschen weiter"

    def latch_clock(self):
        """Steigende Flanke an ST_CP: Schieberegister -> Ausgangsregister."""
        self.store = self.shift[:]
        self.last_action = "↑ ST_CP: Alle 8 Bits gleichzeitig ins Ausgangsregister übernommen"

    def toggle_oe(self):
        self.oe = 1 - self.oe
        self.last_action = "OE umgeschaltet: Ausgänge aktiv" if self.oe == 0 else "OE umgeschaltet: Ausgänge hochohmig / deaktiviert"


class App:
    def __init__(self):
        pygame.init()
        pygame.display.set_caption("74HC595 - Bewegung im Schieberegister")
        self.screen = pygame.display.set_mode((WIDTH, HEIGHT))
        self.clock = pygame.time.Clock()

